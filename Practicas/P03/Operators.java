public class Operators{
	public static void main(String[] args){
		int result = 0;
		
		result += 3;//Suma mas asignación
		System.out.println("result is now "+ result);

		result -= 1;// OperAsig Decremento
		System.out.println("result is now "+ result);

		result *= 2;//Multi mas asignación
		System.out.println("result is now "+ result);

		result /= 2;//Div mas asignación
		System.out.println("result is now "+ result);

		result *= 5;//Multi mas asignación
		System.out.println("result is now "+ result);

		result -= 7;//Resta mas asignación
		System.out.println("result is now "+ result);
	}
}